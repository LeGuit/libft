#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	char	*res;

	i = 0;
	while (n != 1)
	{
		i++;
		n /= 10;
	}
	if (n < 0)
		i++;
	res = ft_strnew(i);
	while (n != 0)
	{
		res[i] = (n % 10) + '0';
		i--;
		n /= 10;
	}
	return (res);
}
